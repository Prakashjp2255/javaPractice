class arr3 {
    public static void main(String[] args) {
        // print 10 numbers from an array using for loop 
        // print table 

        int [] save = new int[10];

        for ( int i=0 ; i<=save.length ; i++) {
            System.out.println(i + "x2=" + i*2) ;
        }
    }
}


